export function convertColorTo8BitTriple(color: Color) {
    return [
        Math.max(Math.min(Math.floor(color.red), 255), 0),
        Math.max(Math.min(Math.floor(color.green), 255), 0),
        Math.max(Math.min(Math.floor(color.blue), 255), 0)
    ]
}